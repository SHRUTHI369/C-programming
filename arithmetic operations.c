#include <stdio.h>
int main(){
	int n1,n2;
	printf("Enter two integers:");
	scanf("%d %d",&n1,&n2);
	printf("Sum of two integers =%d\n",n1+n2);
	printf("Difference of two integers =%d\n",n1-n2);
	printf("Product of two integers =%d\n",n1*n2);
	printf("Division of two integers =%d\n",n1/n2);
	printf("Mod of two integers =%d\n",n1%n2);
	
	
}
